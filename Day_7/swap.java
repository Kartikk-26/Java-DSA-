public class swap {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;
        System.out.println("a = " +a + " &" + " b = " +b);

        //Swap the values 

        int temp = a;
        a = b;
        b = temp;

        System.out.println("a = " +a + " &" + " b = " +b);
    }
    
}
